int GetLogoWidthHeigh(uchar *psData, int *piWidth, int *piHeigh)
{
	uchar	*pRead = psData;

	*piHeigh = (*pRead) * 8;
	pRead++;
	*piWidth = (*pRead)*256;
	pRead++;
	*piWidth += (*pRead);

	return 0;
}

